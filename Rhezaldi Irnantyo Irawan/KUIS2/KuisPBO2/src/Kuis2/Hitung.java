    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        // TODO add your handling code here:
        a = Double.parseDouble(jTextField1.getText());
        b = Double.parseDouble(jTextField2.getText());
        
        if (jComboBox2.getSelectedItem().equals("+")){
            c = a + b;
            String hasil = String.valueOf(c);
            jTextField3.setText(hasil);
        }else if (jComboBox2.getSelectedItem().equals("-")){
            c = a - b;
            String hasil2 = String.valueOf(c);
            jTextField3.setText(hasil2);
        } else if (jComboBox2.getSelectedItem().equals("*")){
            c = a * b;
            String hasil3 = String.valueOf(c);
            jTextField3.setText(hasil3);
        } else if (jComboBox2.getSelectedItem().equals("/")) {
            c = a / b;
            String hasil4 = String.valueOf(c);
            jTextField3.setText(hasil4);
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

