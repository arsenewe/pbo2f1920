    private void operatorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_operatorActionPerformed
        // TODO add your handling code here:
        a = Double.parseDouble(angka1.getText());
        b = Double.parseDouble(angka2.getText());
        
        if (operator.getSelectedItem().equals("+")){
            c = a + b;
            String hasil1 = String.valueOf(c);
            hasil.setText(hasil1);
        }else if (operator.getSelectedItem().equals("-")){
            c = a - b;
            String hasil2 = String.valueOf(c);
            hasil.setText(hasil2);
        } else if (operator.getSelectedItem().equals("*")){
            c = a * b;
            String hasil3 = String.valueOf(c);
            hasil.setText(hasil3);
        } else if (operator.getSelectedItem().equals("/")) {
            c = a / b;
            String hasil4 = String.valueOf(c);
            hasil.setText(hasil4);
        }
    }//GEN-LAST:event_operatorActionPerformed

