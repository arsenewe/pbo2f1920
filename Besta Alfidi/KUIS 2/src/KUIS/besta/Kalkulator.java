    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:
        buka_koneksi();
        String sqlkode="Insert into `record data`(angka1,angka2,operator,hasil)"
        + "values ('"+this.angka1.getText()+"',"
        + "'"+this.angka2.getText()+"',"
        + "'"+this.operator.getSelectedItem()+"',"
        + "'"+this.hasil.getText()+"')";
        try {
            PreparedStatement mStatement = koneksi.prepareStatement(sqlkode);
            mStatement.executeUpdate();
            mStatement.close();
            JOptionPane.showMessageDialog(this,"Data Berhasil Ditambah");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Terjadi Kesalahan "+ex.getMessage());
        }
        
        new Hasil().setVisible(true);
    }//GEN-LAST:event_nextActionPerformed

